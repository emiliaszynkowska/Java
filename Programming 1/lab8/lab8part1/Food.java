/* Create the Food class
 */
public class Food {

/* Initialise the instance variable name
 */
  public String name;

/* Constructor which takes a string parameter
 * Sets the input to name
 */
  public Food(String a) {
    name = a;
  }

/* Method getName() return the name of the current instance
 */
  public String getName() {
    return this.name;
  }

}
